                        <p>Right now, I am working on my own project called Hamudodi series. Hamudodi simply means that “Hãy Mau Đọc Đi”. A direct translation into English is “Read! Now!”. I strongly believe that only through readings can people acquire knowledge and grow intellectually. However, there are few problems. Firstly, there are so many books and there is so little time for us to read them. In addition to that, not all of the books are actually worthy of reading. Secondly, there are so many good books but not many of us are willing to read them. Finally, there are so many good books, at the same time, there are so many underprivileged people, whose life may have been changed through knowledge gained from books, do not have access to those well-written books.</p>

